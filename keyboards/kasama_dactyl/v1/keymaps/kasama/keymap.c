bool combo_should_trigger(uint16_t combo_index, combo_t *combo, uint16_t keycode, keyrecord_t *record) {
    if (get_highest_layer(layer_state) == _GAME || get_highest_layer(layer_state) == _GAME_NUM_2) {
        return false;
    }

    return true;
}

