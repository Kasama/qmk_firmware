void raw_hid_receive(uint8_t *data, uint8_t length) {
    uint8_t response[length];
    memset(response, 0, length);

    if (data[0] == 0x41) {
        raw_hid_send(response, length);
    } else if (data[0] == 0x42) {
        bootloader_jump();
    } else if (data[0] == 0x43) {
        response[0] = 0x43;
        response[1] = get_highest_layer(layer_state);
        raw_hid_send(response, length);
    } else if (data[0] == 0x44) {
        layer_move(data[1]);
        response[0] = 0x44;
        response[1] = get_highest_layer(layer_state);
        raw_hid_send(response, length);
    } else if (data[0] == 0x45) {
        uint8_t state = data[1];
        uint8_t row = data[2];
        uint8_t col = data[3];
        bool pressed = state > 0;

        keyevent_t event = MAKE_KEYEVENT(row, col, pressed);

        action_exec(event);

        response[0] = 0x44;
        response[1] = get_highest_layer(layer_state);
        // response[2] = (keycode >> 8) & 0xFF;
        // response[3] = keycode & 0xFF;
        response[4] = 0;
        response[5] = 0xBA;
        response[6] = 0;
        response[7] = (TO(1) >> 8) & 0xFF;
        response[8] = TO(1) & 0xFF;
        raw_hid_send(response, length);
    }
}
